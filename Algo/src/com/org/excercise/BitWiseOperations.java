package com.org.excercise;

public class BitWiseOperations {

	public static void main(String[] args) {
		String a="";
		int a=45;
		byte b=45;
		char c='a';
		short d=45;
		long e=45;
		System.out.println(a+" - "+b+" - "+c+" - "+d+" - "+e);
		System.out.println(~a+" - "+~b+" - "+~c+" - "+~d+" - "+e);

	}

}
